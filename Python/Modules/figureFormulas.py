def CalculateAreaOfTriangle(a, b, c):
    return (a + b + c) / 2

def CalculateAreaOfSquare(a):
    return a * a

def CalculateAreaOfRectangle(a, b):
    return a * b

def CalculateAreaOfRhombus(d1, d2):
    return (d1 * d2) / 2    

def CalculateAreaOfTrapezoid(a, b, h):
    return ((a + b) / 2) * h 